from datetime import date

# пожелания на каждый день (0=понедельник, 6=воскресенье)
greetings = [
    "Хорошего понедельника",
    "Хорошего вторника",
    "Хорошей среды",
    "Хорошего четверга",
    "Хорошей пятницы",
    "Хорошей субботы",
    "Хорошего воскресенья",
]

def today_wish():
    """возвращает пожелание для текущего дня"""
    return greetings[date.today().weekday()]

def greet_user(name):
    """собирает приветствие с пожеланием"""
    return f"Привет, {name}. {today_wish()}!"